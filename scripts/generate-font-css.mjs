import { createHash } from 'node:crypto';
import { execFile } from 'node:child_process';
import { promisify } from 'node:util';
import { mkdir, mkdtemp, readdir, readFile, rm, writeFile } from 'node:fs/promises';
import os from 'node:os';
import path from 'node:path';
import subsetFont from 'subset-font';

const execFileAsync = promisify(execFile);
const root = process.cwd();
const wranglerBin = path.join(root, 'node_modules/.bin/wrangler');
const cssOutputPath = path.join(root, 'src/lib/generated/fonts.css');
const linksOutputPath = path.join(root, 'src/lib/generated/font-links.ts');

const fontAssetOrigin = trimTrailingSlash(process.env.PUBLIC_FONT_ASSET_ORIGIN);
const cjkFontAssetOrigin = trimTrailingSlash(process.env.PUBLIC_CJK_FONT_ASSET_ORIGIN);
const fontR2Bucket = process.env.FONT_R2_BUCKET ?? 'fonts';

const cjkSourceDirs = ['src/lib/i18n/zh', 'src/lib/i18n/i18n-util.ts', 'src/lib/ui', 'src/routes'];

const iosevkaFiles = [
	['Iosevka Aile Web', 400, 'IosevkaAile-Regular.woff2'],
	['Iosevka Aile Web', 500, 'IosevkaAile-Medium.woff2'],
	['Iosevka Aile Web', 600, 'IosevkaAile-SemiBold.woff2'],
	['Iosevka Aile Web', 700, 'IosevkaAile-Bold.woff2'],
	['Iosevka Term Web', 400, 'IosevkaTerm-Regular.woff2'],
	['Iosevka Term Web', 500, 'IosevkaTerm-Medium.woff2'],
	['Iosevka Term Web', 600, 'IosevkaTerm-SemiBold.woff2'],
	['Iosevka Term Web', 700, 'IosevkaTerm-Bold.woff2']
];

const sourceHanSansVersion = '2.004';
const sourceHanSansFiles = [
	[400, 'SourceHanSansCN-Regular.otf'],
	[500, 'SourceHanSansCN-Medium.otf'],
	[600, 'SourceHanSansCN-Medium.otf'],
	[700, 'SourceHanSansCN-Bold.otf']
];

await mkdir(path.dirname(cssOutputPath), { recursive: true });

const css = [
	'/* Generated by scripts/generate-font-css.mjs. Do not edit manually. */',
	createIosevkaFontFaces(),
	await createSourceHanSansFontFaces()
]
	.filter(Boolean)
	.join('\n\n');

const stylesheetHrefs = [];

await Promise.all([
	writeFile(cssOutputPath, `${css}\n`),
	writeFile(
		linksOutputPath,
		[
			'/* Generated by scripts/generate-font-css.mjs. Do not edit manually. */',
			`export const fontStylesheetHrefs = ${JSON.stringify(stylesheetHrefs, null, '\t')} as const;`,
			''
		].join('\n')
	)
]);

function trimTrailingSlash(value) {
	return value?.replace(/\/+$/, '');
}

async function createSourceHanSansFontFaces() {
	if (!cjkFontAssetOrigin) {
		return '';
	}

	const chars = await collectCjkCharacters();
	if (!chars) {
		return '';
	}

	const hash = createHash('sha256')
		.update(sourceHanSansVersion)
		.update('\0')
		.update(chars)
		.digest('hex')
		.slice(0, 16);

	const fontFaces = [];

	for (const [weight, filename] of sourceHanSansFiles) {
		const subsetKey = [
			'subsets/source-han-sans-cn',
			sourceHanSansVersion,
			hash,
			`${path.basename(filename, '.otf')}.woff2`
		].join('/');

		if (!(await ensureSourceHanSansSubset(chars, filename, subsetKey))) {
			continue;
		}

		fontFaces.push(
			[
				'@font-face {',
				"\tfont-family: 'Source Han Sans SC Web';",
				'\tfont-display: swap;',
				`\tfont-weight: ${weight};`,
				'\tfont-stretch: normal;',
				'\tfont-style: normal;',
				`\tsrc: url('${cjkFontAssetOrigin}/${subsetKey}') format('woff2');`,
				'}'
			].join('\n')
		);
	}

	return fontFaces.join('\n\n');
}

function createIosevkaFontFaces() {
	if (!fontAssetOrigin) {
		return '';
	}

	return iosevkaFiles
		.map(([family, weight, filename]) => {
			const url = `${fontAssetOrigin}/fonts/iosevka/34.6.1/${filename}`;

			return [
				'@font-face {',
				`\tfont-family: '${family}';`,
				'\tfont-display: swap;',
				`\tfont-weight: ${weight};`,
				'\tfont-stretch: normal;',
				'\tfont-style: normal;',
				`\tsrc: url('${url}') format('woff2');`,
				'}'
			].join('\n');
		})
		.join('\n\n');
}

async function ensureSourceHanSansSubset(chars, filename, subsetKey) {
	if (await r2ObjectExists(subsetKey)) {
		return true;
	}

	const sourceKey = [
		'source/source-han-sans-cn',
		sourceHanSansVersion,
		filename
	].join('/');
	const sourceFont = await getR2Object(sourceKey);
	const subset = await subsetFont(Buffer.from(sourceFont), chars, {
		targetFormat: 'woff2'
	});

	await putR2Object(subsetKey, subset, {
		'content-type': 'font/woff2',
		'cache-control': 'public, max-age=31536000, immutable'
	});

	return true;
}

async function r2ObjectExists(key) {
	try {
		await getR2Object(key);
		return true;
	} catch (error) {
		if (isMissingR2ObjectError(error)) {
			return false;
		}

		throw error;
	}
}

async function getR2Object(key) {
	const tempDir = await mkdtemp(path.join(os.tmpdir(), 'radio-fonts-'));
	const tempFile = path.join(tempDir, 'object');

	try {
		await runWrangler(['r2', 'object', 'get', `${fontR2Bucket}/${key}`, '--file', tempFile, '--remote']);
		return await readFile(tempFile);
	} finally {
		await rm(tempDir, { force: true, recursive: true });
	}
}

async function putR2Object(key, body, headers) {
	const tempDir = await mkdtemp(path.join(os.tmpdir(), 'radio-fonts-'));
	const tempFile = path.join(tempDir, 'object');

	try {
		await writeFile(tempFile, body);
		await runWrangler([
			'r2',
			'object',
			'put',
			`${fontR2Bucket}/${key}`,
			'--file',
			tempFile,
			'--remote',
			'--content-type',
			headers['content-type'],
			'--cache-control',
			headers['cache-control']
		]);
	} finally {
		await rm(tempDir, { force: true, recursive: true });
	}
}

async function runWrangler(args) {
	const attempts = 3;

	for (let attempt = 1; attempt <= attempts; attempt += 1) {
		try {
			await execFileAsync(wranglerBin, args, {
				cwd: root,
				maxBuffer: 1024 * 1024 * 8
			});
			return;
		} catch (error) {
			const stderr = error.stderr ? String(error.stderr) : '';
			const stdout = error.stdout ? String(error.stdout) : '';
			error.message = [error.message, stdout, stderr].filter(Boolean).join('\n');

			if (!isRetryableWranglerError(error) || attempt === attempts) {
				throw error;
			}

			await delay(attempt * 1000);
		}
	}
}

function isMissingR2ObjectError(error) {
	return /specified key does not exist|not found|404/i.test(error.message);
}

function isRetryableWranglerError(error) {
	return /fetch failed|network|connectivity|timeout|ETIMEDOUT|ECONNRESET|ENOTFOUND/i.test(
		error.message
	);
}

function delay(ms) {
	return new Promise((resolve) => {
		setTimeout(resolve, ms);
	});
}

async function collectCjkCharacters() {
	const files = [];

	for (const source of cjkSourceDirs) {
		await collectFiles(path.join(root, source), files);
	}

	const chars = new Set();

	for (const file of files) {
		const content = await readFile(file, 'utf8');

		for (const char of content.matchAll(
			/[\u3000-\u303f\uff00-\uffef\u3400-\u4dbf\u4e00-\u9fff]/gu
		)) {
			chars.add(char[0]);
		}
	}

	return [...chars].sort().join('');
}

async function collectFiles(target, files) {
	let entries;

	try {
		entries = await readdir(target, { withFileTypes: true });
	} catch {
		if (/\.(svelte|ts|js)$/.test(target)) {
			files.push(target);
		}

		return;
	}

	for (const entry of entries) {
		const child = path.join(target, entry.name);

		if (entry.isDirectory()) {
			await collectFiles(child, files);
			continue;
		}

		if (/\.(svelte|ts|js)$/.test(entry.name)) {
			files.push(child);
		}
	}
}
